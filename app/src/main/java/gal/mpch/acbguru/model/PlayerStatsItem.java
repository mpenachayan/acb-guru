package gal.mpch.acbguru.model;

public class PlayerStatsItem {
    String name;

    String number;

    boolean playing;

    String time;

    String points;

    String rebounds;

    String assists;

    String foulsCommitted;

    String blocks;

    String valorization;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public boolean isPlaying() {
        return playing;
    }

    public void setPlaying(boolean playing) {
        this.playing = playing;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getPoints() {
        return points;
    }

    public void setPoints(String points) {
        this.points = points;
    }

    public String getRebounds() {
        return rebounds;
    }

    public void setRebounds(String rebounds) {
        this.rebounds = rebounds;
    }

    public String getAssists() {
        return assists;
    }

    public void setAssists(String assists) {
        this.assists = assists;
    }

    public String getFoulsCommitted() {
        return foulsCommitted;
    }

    public void setFoulsCommitted(String foulsCommitted) {
        this.foulsCommitted = foulsCommitted;
    }

    public String getBlocks() {
        return blocks;
    }

    public void setBlocks(String blocks) {
        this.blocks = blocks;
    }

    public String getValorization() {
        return valorization;
    }

    public void setValorization(String valorization) {
        this.valorization = valorization;
    }

}